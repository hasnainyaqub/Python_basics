# Arithmetic operators (+,-,*,/,%,**)
a = 5
b = 2
print(a+b)  #Addition 
print(a-b)  #Subtraction
print(a*b)  #Multiplication
print(a/b)  #Division
print(a%b)  #remainder
print(a**b) #power

# Relational operator 
a = 80 
b = 30

print(a == b) #Equal to
print(a != b) #Not Equal to
print(a >  b) #Greater than
print(a <  b) #Less than
print(a >= b) #Greater than Equal to
print(a <= b) #less than Equal to

